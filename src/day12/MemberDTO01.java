package day12;

public class MemberDTO01 {
	private int menu;
	private int ice;
	private int size;
	
	public void setMenu(int menu) {
		this.menu = menu;
	}
	
	public void setIce(int ice) {
		this.ice = ice;
	}
	public void setSize(int size) {
		this.size = size;
	}
	public int getMenu() {
		return menu;
	}
	
	public int getIce() {
		return ice;
	}
	public int getSize() {
		return size;
	}
}
